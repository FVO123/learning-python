# -*- coding: utf-8 -*-


def add(first, second):
    return first + second


def subtract(first, second):
    return first - second


def multiply(first, second):
    return first * second


def divide(first, second):
    return first / second


def maximum(numbers):
    return max(numbers)


def minimum(numbers):
    return min(numbers)


def is_positive(number):
    return number > 0


def add_all(*numbers):
    return sum(numbers)
